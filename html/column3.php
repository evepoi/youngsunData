    <h2>미술작품에 대한 투자는 절세(折稅)의 미학(美學)이다!</h2>
    <h3>
        최근 미술작품에 대한 관심이 높아지고 있다.
        2021년 연초에 개막된 서울화랑미술제에서 미술품이 역대 매출가를 기록했으며,
        4월 초에 개막한 부산화랑아트페어(BAMA)에서도 좋은 성과를 올릴 것으로 알려졌다.
        또한 그림을 구매하는 연령층이 점차 변화하고 있다.<br>
        2007년만 해도 그림의 최대 구매자는 50~60대가 주류였다고 볼 수 있었으나 최근의 최대 구매자는 30대이다.
        50~60대가 묻지마 투자였다면 최근의 30대 구매자는 열심히 공부하고 발품을 파는 현명한 투자 구매자이다.
        그동안 코로나19에 억눌렸던 소비심리가 분출된 것도 있지만,
        최근에는 이른바 “아트테크(아트+재테크)”에 대한 소비자의 관심이 높아진 것으로 전문가들은 평가하고 있다.
        미술품에 관심이 집중되는 또 다른 이유는 조세정책과 관련이 있다.
        최근 정부에서 부동산과 주식투자에 대한 과세가 강화되고 있다.
        그러나 미술품 구매에는 아직은 세금부담이 적기 때문에 미술품에 투자하는 사람이 늘어난 것이다.</h3>
    <h4>여기서 미술품에 부과되는 세금에 대해서 알아보면 다음과 같다.</h4>
    <h4>첫째. 미술품은 양도가액 즉 매도금액이 6,000만원 미만이면 세금이 없다.
        여기서 필요경비도 양도가액의 최대 90%까지 인정된다.
        2021년부터는 빈번한 미술작품 거래에 대해서도 고율과세를 하지 않기로 세금제도가 바뀌어서 우리가 말하는 아트테크에 주목하고 관심을 가져야 할 이유가 생겼다.</h4>
    <h4>둘째. 미술품에 대한 투자에서 세제상 유리한 요소는 우선 그림을 양도할 때에만 세금을 낸다는 특징이 있다.
        부동산의 경우 팔 때 양도소극세는 물론 살 때는 취득세와 등록세를, 보유할 때는 재산세와 종합부동산세를 내야하며,
        주식도 매각할 때 증권거래세를 내야한다.
        특히 2023년부터는 주식투자소득이 5,000만원을 넘으면 양도소극세(금융투자소득세)도 납부해야 한다.
        반면에 미술작품을 팔아서 생긴 소득은 소득세법상 “기타소득”으로 분류되어 과세되는데 세율은 지방세를 포함하여 22%에 불과하다.
        다시 말해서 양도차익이 아니라 양도가액에 세금을 부과한다는 의미이다.
        이럴 경우 “취득가액을 고려하지 않으니 세제상 불리한 것 아닌가?”라고 생각할 수 있지만
        반대로 비과세감면이 많아 여러 가지 불리한 점을 상당부분 상쇄해준다고 볼 수 있다.</h4>
    <h4>셋째. 미술품은 양도가액이 6,000만원 미만이면 비과세이다.
        아울러 양도가액이 6,000만원 이상인 경우에도 세금부담이 그리 크지 않는데 왜냐하면 필요경비율이 80%로 높기 때문이다.
        또한 양도가액이 1억원 이하이거나 보유기간이 10년 시상이 되면 필요경비율이 90%로 높아져서 세금은 더욱 줄어든다.
        예를 들어 미술작품을 5,000만원에 사서 5년 보유한 뒤 8,000만원에 팔면 양도가액이 1억원 미만이라 필요경비를 90%까지 공제되어
        90%에 대항하는 7,200만원은 필요경비로 공제되고 너미지 10%인 800만원(과세 표준)에만 세금이 부과된다는 의미이다.
        여기에 세율을 곱하면 소득세는(지방소득세 포함) 176만원이 나오며 양도차익 3,000만원의 약 6%가 안 되는 금액이다.</h4>
    <h4>넷째. 살아있는 국내 작가의 작품은 작품가격에 상관없이 비과세로 세금이 없다.
        잠재력과 가능성이 있는 중견작가의 작품에 투자했다가 나중에 그림 가치가 올라갔을 때 매도하여 많은 투자 수익을 극대화할 수 있다고 볼 수 있다.
        통상적으로 아트테크로 하는 천만원 정도의 중견작가 30~40호 크기의 작품을 구입하면 나중에 큰 부가가치를 창출할 수 있늘 것이라고 생각된다.
        아울러 소득세법에 명시된 과세 대상 미술품은 회화, 데생, 파스텔, 오리지널 판화 ․ 인쇄화, 골동품 등으로 명시되어 있어
        조각품은 과세대상이 아니라는 점도 주목해둘 필요가 있다.</h4>
    <h4>다섯째. 2021년에는 고율과세 리스크가 해도되는 점도 주목할 필요가 있다.
        작년(2020년)에 이른바 “사업소득 과세 리스크”가 해소된 것도 미술작품 투자자들에게는 긍정적인 요인이다.
        미술품 투자 소득은 기타소득과세가 원칙이지만 거래 빈도가 높으면 사업소득으로 과세되는 경우가 있었는데
        그동안 ‘영리 목적으로 반복적으로 행하는 활동을 통해 얻은 소득은 사업소득으로 본다’는 소득세법이 적용되어 사업소득 최고세유이 46.2%(지방세포함)이었다.
        세율이 22%인 기타소득보다 크게 높았다고 볼 수 있다.<br>
        이러한 불합리한 규정에 대한 미술투자자들은 그동안 지속적으로 불만을 제기해왔는데 다행히 지난해 말 소득세법을 고쳐 거래빈도와
        상관없이 미술품 투자소득은 기타소득으로 간주하도록 했다. 이때 바뀐 규정은 2021년 1월부터 시행되고 있다.<br>
        미술품 거래를 위해 사업장을 설치하거나 사업자 등록을 한 경우에만 사업소득세가 적용된다고 할 수 있다.
        미술품 양도소득의 과세 방식은 원천징수방식이다. 그림을 사는 사람이 과세당국에 세금을 내고, 판매자는 원천칭수 영수증을 잘 챙겨 놓으면 된다.</h4>
    <h4>여섯째. 법인 사업자가 작품을 구매할 경우 1점당 1,000만원 이하에 한하여 비용(손금산입)처리가 가능하며
        양도차익에 대하여 법인세가 부과된다.
        또한 법인사업자의 경우 장식이나 환경미화 목적으로 작품을 대여하여 설치한 경우 전액 비용처리가 된다는 점도 주목할 필요가 있다.</h4>
    <h4>마지막으로 미술작품에 투자해도 좋은 이유를 살펴보면 다음과 같다. (박정수의 미술사색에서 발췌)</h4>
    <h3>01 절세수단이 될 수 있다.</h3>
    <h4>- 미술품의 경우 6,000만원 이상일 경우 양도차익 20%의 22%가 양도세이다.</h4>
    <h3>02 미술품은 도둑을 맞아도 언젠가는 찾는다.</h3>
    <h4>- 소유주가 누구였는지 밝혀져야 거래가 가능하다.</h4>
    <h3>03 잘 고른 미술품은 가격상승률이 매우 높다.</h3>
    <h3>04 향후 담보대출이 가능한 동산으로 취급된다.</h3>
    <h3>05 안전한 현금보관 품목이다.</h3>
    <h3>06 이동과 전달이 용이하다.</h3>
    <h3>07 원작소유의 즐거움이 있다.</h3>
    <h3>08 생산자에 대한 투자가 가능하다.</h3>
    <h3>09 일정 수준에 도달하면 가격하락이 없다.</h3>
    <h3>10 대중의 기호에 영향을 받지 않는다.</h3>
    <h3>11 주식보다 원금 보전에 유리하다.</h3>
    <h3>12 부동산보다 적은 금액으로 투자할 수 있다.</h3>
    <h4>결론적으로 말하면 미술작품은 소유하면서 마음껏 감상하고 즐기며 추후에 높은 가격으로 판매할 수 있다는 점이 매력적이다.
        그러나 아트테크는 남의 말과 추천에만 의지하지 말고 좋아하는 작품선택 시 발과 눈과 가슴으로 해야 한다는 점을 명심해야 할 것이다.</h4>
