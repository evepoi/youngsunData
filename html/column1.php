    <h2>미술작품 투자는 어떻게 할 것인가</h2>
    <h4>
        작년에 개최되었던 국내 최대 미술장터인 한국국제아트페어(KIAF)에서 650억원의 역대 최고 매출액과 아울러 방문객 8만8천명(중복 입장 포함)의 대기록을 세웠다.<br>
        이 행사의 첫 방문객의 연령 분포를 보면 MZ세대인 21~40세가 60.4%로 반 이상을 차지했고 그다음으로는 40~50대가 33.8%, 60대 이상이 3.1%를 기록했다.(키아프 서울 2021 리포트 참조)<br>
        또 화랑협회는 방문객 응답자 설문조사를 통해 젊은 컬렉터의 폭증과 새로운 컬렉터들의 미술시장에 대한 관심을 확인할 수 있었다고 설명했다.<br>
        특히 2022년 키아프는 세계 3대 아트페어인 하나인 영국의 프리즈와 공동개최할 예정으로 한국 미술계의 태풍급 변화를 예고하고 있다.<br>
        <br>
        그러면 2022년 키아프 서울에서 기대되는 장르로 응답자들은 무엇을 꼽았을까?<br>
        국내와 해외 젊은 작가들의 작품이라고 답한 비중이 각각 57.4%와 46.2%로 높게 나타났다.<br>
        또한 한국 현대 미술이라는 응답도 43.4%나 됐다. 프리즈와 공동으로 개최할 예정인 2022년 키아프의 출품 경향을 짐작해볼 수 있는 대목이다.<br></h4>
