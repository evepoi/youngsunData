        다음으로 드로잉이 33%로 2위, 조각과 사진이 각각 22.2%와 15.9%로 3위와 4위로 조사되었다.<br>
        프린트 작품(13.5%)과 설치 작품(13.1%)의 선호도도 높게 나왔으며 이어 NFT 작품(11.9%), 미더어 아트(10.8%) 등의 순서였다.<br>
